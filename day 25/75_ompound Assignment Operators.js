"use strict";
function useCompoundOperators() {
    let x = 10;
    console.log("Initial x:", x);
    x += 5; // Adds 
    console.log("After addition:", x);
    x -= 3; // Subtracts 
    console.log("After subtraction:", x);
    x *= 2; // Multiplies 
    console.log("After multiplication:", x);
    x /= 4; // Divides 
    console.log("After division:", x);
}
useCompoundOperators();
